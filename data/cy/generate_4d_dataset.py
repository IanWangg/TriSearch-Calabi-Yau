#!/usr/bin/env python3
from __future__ import annotations

import argparse
import json
import sys
from pathlib import Path

if __package__ is None or __package__ == "":
    repo_root = Path(__file__).resolve().parents[2]
    if str(repo_root) not in sys.path:
        sys.path.insert(0, str(repo_root))

from data.cy.pipeline import generate_and_save_cy_4d_reflexive_dataset_incremental


def _build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(
        description=(
            "Generate a 4D CY dataset from fetched N-lattice reflexive polytopes: "
            "FRST seeds from random_triangulations_fair, then BFS-based random samples "
            "or random-flip samples with distance to the source nearest FRST."
        )
    )
    parser.add_argument(
        "--polytope-file",
        "--polytope_file",
        dest="polytope_file",
        type=str,
        default=None,
        help=(
            "Optional JSON/JSONL file containing a saved set of 4D N-lattice polytopes. "
            "When provided, the generator reuses those polytopes instead of calling fetch_polytopes."
        ),
    )
    parser.add_argument(
        "--num-polytopes",
        type=int,
        required=False,
        default=None,
        help="Number of 4D reflexive polytopes to fetch, or an optional cap when --polytope-file is provided.",
    )
    parser.add_argument(
        "--h11",
        type=int,
        required=False,
        default=None,
        help="Required h11 value for fetched fourfold polytopes. Ignored when --polytope-file is provided.",
    )
    parser.add_argument(
        "--num-vertices",
        type=int,
        default=None,
        help="Optional filter on the number of vertices for fetched fourfold polytopes.",
    )
    parser.add_argument(
        "--favorable",
        action=argparse.BooleanOptionalAction,
        default=None,
        help="Whether to filter favorable fourfold polytopes. When omitted, no favorability filter is applied.",
    )
    parser.add_argument(
        "--num-triangulations-per-frst",
        "--num_triangulations_per_frst",
        "--triangulations-per-frst",
        "--triangulations_per_frst",
        "--triangulations-per-polytope",
        "--triangulations_per_polytope",
        "--k",
        dest="num_triangulations_per_frst",
        type=int,
        required=True,
        help="Number of non-fine triangulations to collect per FRST seed (legacy alias: --triangulations-per-polytope).",
    )
    parser.add_argument("--seed", type=int, default=0, help="Random seed for reproducible sampling.")
    parser.add_argument("--output-dir", type=str, default="./output4d", help="Directory for dataset outputs.")
    parser.add_argument("--output-name", type=str, default="cy_reflexive_dataset_4d", help="Output filename stem.")
    parser.add_argument(
        "--compact-output",
        "--compact_output",
        dest="compact_output",
        action="store_true",
        help="Save only <output-name>.samples.jsonl and skip the large full dataset JSON.",
    )
    parser.add_argument("--triangulation-backend", type=str, default="qhull", help="cytools triangulation backend.")
    parser.add_argument("--neighbor-backend", type=str, default=None, help="cytools neighbor backend.")
    parser.add_argument(
        "--frsts-per-polytope",
        "--num-frsts-per-polytope",
        "--num_frsts_per_polytope",
        type=int,
        default=None,
        help="Number of FRST seeds generated by random_triangulations_fair per polytope. Defaults to num-triangulations-per-frst.",
    )
    parser.add_argument(
        "--fair-backend",
        type=str,
        default="cgal",
        help="Primary backend for random_triangulations_fair FRST generation.",
    )
    parser.add_argument(
        "--fair-backend-fallback",
        type=str,
        default="cgal",
        help="Fallback backend when fair-backend fails.",
    )
    parser.add_argument(
        "--fair-max-retries",
        type=int,
        default=100,
        help="max_retries parameter passed into random_triangulations_fair.",
    )
    parser.add_argument(
        "--fair-max-attempt-rounds",
        type=int,
        default=10,
        help="How many FRST generation rounds to attempt per polytope.",
    )
    parser.add_argument(
        "--fair-call-timeout-seconds",
        type=float,
        default=120,
        help=(
            "Optional wall-clock timeout (seconds) for each random_triangulations_fair call. "
            "On timeout, fair sampling is skipped and fast fallback is used."
        ),
    )
    parser.add_argument(
        "--fast",
        action="store_true",
        help="Use random_triangulations_fast only for FRST seed generation (skip random_triangulations_fair).",
    )
    parser.add_argument(
        "--include-points-interior-to-facets",
        action=argparse.BooleanOptionalAction,
        default=True,
        help="Whether to include points interior to facets when triangulating.",
    )
    parser.add_argument(
        "--make-star",
        action=argparse.BooleanOptionalAction,
        default=True,
        help="Whether to request star triangulations from cytools when generating random-height triangulations.",
    )
    parser.add_argument(
        "--max-retries-per-triangulation",
        type=int,
        default=4,
        help="Legacy parameter kept for compatibility (not used in FRST-first mode).",
    )
    parser.add_argument(
        "--height-scale",
        type=float,
        default=1.0,
        help="Legacy parameter kept for compatibility (not used in FRST-first mode).",
    )
    parser.add_argument(
        "--bfs-max-depth",
        "--max-depth",
        "--max_depth",
        dest="bfs_max_depth",
        type=int,
        default=4,
        help="Maximum BFS depth for nearest FRST search.",
    )
    parser.add_argument(
        "--bfs-max-nodes",
        "--max-nodes",
        "--max_nodes",
        "--max-node",
        "--max_node",
        dest="bfs_max_nodes",
        type=int,
        default=300,
        help="Maximum number of BFS nodes to visit/expand.",
    )
    parser.add_argument(
        "--collection-depths",
        "--collection_depths",
        type=int,
        nargs="+",
        default=None,
        help="Collect only non-FRST states at these exact BFS depths from FRST seeds (ignored with --random_flip).",
    )
    parser.add_argument(
        "--collection-all",
        "--collection_all",
        dest="collection_all",
        action="store_true",
        help="Collect all states found at collection-depths. If unset, randomly subsample to num-triangulations-per-frst.",
    )
    parser.add_argument(
        "--random-flip",
        "--random_flip",
        dest="random_flip",
        action="store_true",
        help=(
            "Use random flip collection instead of BFS: for each FRST, repeatedly sample "
            "triangulations via random_flips(N=max-depth, only_regular=True). "
            "When enabled, --collection-depths is ignored."
        ),
    )
    parser.add_argument(
        "--num-workers",
        type=int,
        default=None,
        help="Number of worker processes for per-polytope collection. Defaults to min(cpu_count, num-polytopes).",
    )
    parser.add_argument(
        "--resume",
        action="store_true",
        help="Resume from checkpoint and append to existing .samples.jsonl output.",
    )
    parser.add_argument(
        "--checkpoint-path",
        type=str,
        default=None,
        help="Optional path to checkpoint JSON. Defaults to <output-dir>/<output-name>.checkpoint.json.",
    )
    parser.add_argument(
        "--log-every",
        type=int,
        default=10,
        help="Log progress every N completed polytopes.",
    )
    parser.add_argument("--triangulation-verbosity", type=int, default=0, help="cytools triangulation verbosity.")
    return parser


def main():
    parser = _build_parser()
    args = parser.parse_args()

    run_result = generate_and_save_cy_4d_reflexive_dataset_incremental(
        num_polytopes=args.num_polytopes,
        h11=args.h11,
        num_vertices=args.num_vertices,
        favorable=args.favorable,
        num_triangulations_per_frst=args.num_triangulations_per_frst,
        polytope_file=args.polytope_file,
        seed=args.seed,
        triangulation_backend=args.triangulation_backend,
        neighbor_backend=args.neighbor_backend,
        include_points_interior_to_facets=args.include_points_interior_to_facets,
        make_star=args.make_star,
        max_retries_per_triangulation=args.max_retries_per_triangulation,
        height_scale=args.height_scale,
        frsts_per_polytope=args.frsts_per_polytope,
        fair_backend=args.fair_backend,
        fair_backend_fallback=args.fair_backend_fallback,
        fair_max_retries=args.fair_max_retries,
        fair_max_attempt_rounds=args.fair_max_attempt_rounds,
        fair_call_timeout_seconds=args.fair_call_timeout_seconds,
        fast=args.fast,
        bfs_max_depth=args.bfs_max_depth,
        bfs_max_nodes=args.bfs_max_nodes,
        collection_depths=args.collection_depths,
        collection_all=args.collection_all,
        random_flip=args.random_flip,
        triangulation_verbosity=args.triangulation_verbosity,
        num_workers=args.num_workers,
        output_dir=args.output_dir,
        output_name=args.output_name,
        compact_output=args.compact_output,
        resume=args.resume,
        checkpoint_path=args.checkpoint_path,
        log_every=args.log_every,
    )
    paths = run_result["paths"]
    summary = run_result["summary"]

    print("4D CY dataset generation complete.")
    print("Saved files:")
    for key, value in paths.items():
        print(f"  - {key}: {value}")
    print("Summary:")
    print(json.dumps(summary, indent=2))


if __name__ == "__main__":
    main()
